public class switchDemo {
    public static void main(String[] args) {

        int liczba = 12;

        switch (liczba){
            case 11:
                System.out.println("11");
                break;
            case 12:
                System.out.println("12");
                break;
            default:
                System.out.println("nie ma 11 anie 12");
        }
    }
}
